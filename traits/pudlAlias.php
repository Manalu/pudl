<?php


////////////////////////////////////////////////////////////////////////////////
//DYNAMICALLY CREATE A UNIQUE QUERY ALIAS
////////////////////////////////////////////////////////////////////////////////
trait pudlAlias {




	////////////////////////////////////////////////////////////////////////////
	//GET A UNQIUE ALIAS
	////////////////////////////////////////////////////////////////////////////
	protected function _alias() {
		return $this->_table('x_pudl_alias_' . ++self::$_aliases);
	}




	////////////////////////////////////////////////////////////////////////////
	//PRIVATE VARIABLES - UNIQUE ALIASES
	////////////////////////////////////////////////////////////////////////////
	private static $_aliases = 0;

}
