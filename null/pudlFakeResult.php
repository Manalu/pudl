<?php


require_once(is_owner(__DIR__.'/pudlNullResult.php'));




class		pudlFakeResult
	extends	pudlNullResult {




	////////////////////////////////////////////////////////////////////////////
	// CONSTRUCTOR - PASS IN SOME "FAKE" DATA (COLUMNS WITH NO ACTUAL DATA)
	////////////////////////////////////////////////////////////////////////////
	public function __construct(pudl $pudl, $fake) {
		foreach ($fake as $item) {
			$this->fake[] = (object)['name' => $item];
		}

		parent::__construct($pudl);
	}




	////////////////////////////////////////////////////////////////////////////
	// GET DETAILS ON ALL FIELD COLUMNS IN THIS RESULT
	////////////////////////////////////////////////////////////////////////////
	public function listFields() {
		return $this->fake;
	}




	////////////////////////////////////////////////////////////////////////////
	// GET THE NUMBER OF FIELD COLUMNS IN THIS RESULT
	////////////////////////////////////////////////////////////////////////////
	public function fields() {
		return count($this->fake);
	}




	////////////////////////////////////////////////////////////////////////////
	// MEMBER VARIABLES
	////////////////////////////////////////////////////////////////////////////
	protected $fake;

}
